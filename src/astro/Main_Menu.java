    private void button_optionsMouseClicked(java.awt.event.MouseEvent evt) {//GEN-FIRST:event_button_optionsMouseClicked
        DefaultTableModel modelo = (DefaultTableModel) tabla.getModel();
        while (modelo.getRowCount() > 0) {
            modelo.removeRow(0);
        }
        tabla.setModel(modelo);
        Object[] row = new Object[7];
        for (int i = 0; i < jugadores.size(); i++) {
            row[0] = jugadores.get(i).getNombre();
            row[1] = jugadores.get(i).getScore();
            row[2] = jugadores.get(i).getShooterCounter();
            row[3] = jugadores.get(i).getBombCounter();
            row[4] = jugadores.get(i).getSunFlowerCounter();
            row[5] = jugadores.get(i).getPotatoCounter();
            row[6] = jugadores.get(i).getTotalPlants();
            modelo.addRow(row);
        }
        tabla.setModel(modelo);
        jd_options.setModal(true);
        jd_options.pack();
        jd_options.setVisible(true);

    }//GEN-LAST:event_button_optionsMouseClicked

