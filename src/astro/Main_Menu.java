    //Metodo para reproducir la musica
    public static void music() throws UnsupportedAudioFileException, IOException, LineUnavailableException {
        File soundFile = new File("./Levels.wav"); //Se encuentra el archivo de musica y se instancia
        AudioInputStream audioInputStream = AudioSystem.getAudioInputStream(soundFile); //Se consigue el InputStream para el archivo de musica
        clip = AudioSystem.getClip(); //Conseguimos un clip para reproducir
        clip.open(audioInputStream); //El compilador abre el clip de musica
        clip.loop(20);
        //clip.start(); //This plays the audio 
    }

    public void lectura() {
        try {
            File archivo = new File("./Jugadores.txt");
            FileReader fr = new FileReader(archivo);
            BufferedReader br = new BufferedReader(fr);
            String linea;
            while ((linea = br.readLine()) != null) {
                String[] lineas = linea.split(",");
                Jugador jugador = new Jugador(lineas[0], Integer.parseInt(lineas[1]), Integer.parseInt(lineas[2]), Integer.parseInt(lineas[3]), Integer.parseInt(lineas[4]), Integer.parseInt(lineas[5]), Integer.parseInt(lineas[6]));
                jugadores.add(jugador);
            }
            fr.close();
        } catch (Exception e) {
            System.out.println("File not found");
        }
    }

